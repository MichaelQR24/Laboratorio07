    private void llenarJList() {
        modeloListCitas = new DefaultListModel();
        JListCitas.setModel(modeloListCitas);
        modeloListCitas.removeAllElements();
         for (Cita cita : listaCitas) {
           
            modeloListCitas.addElement(cita.toString());
        }
    }
    public void addCita(Cita cita){
        listaCitas.add(cita);
        llenarJList();                
    }
