    private void JListCitasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JListCitasMouseClicked
        // TODO add your handling code here:
        int indice = JListCitas.getSelectedIndex();
        if( indice!=-1 && evt.getClickCount() == 2){
            Cita cita = listaCitas.get(indice);
            JFrameEditarCita visualizarCita = new JFrameEditarCita(cita,this);
            visualizarCita.modificarFrame();
            visualizarCita.setLocationRelativeTo(null);
            visualizarCita.setSize(290,355);
            visualizarCita.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_JListCitasMouseClicked

    private void btnCancelarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCancelarMouseClicked
        // TODO add your handling code here:
       int indice = JListCitas.getSelectedIndex();
        if(indice!=-1){
            listaCitas.remove(indice);
            llenarJList();
            JOptionPane.showMessageDialog(null,
                    "Cita eliminada "
                    ,"Eliminar Cita", JOptionPane.INFORMATION_MESSAGE);
        }else{
             JOptionPane.showMessageDialog(null,
                     "Selecciona un cita de la lista"
                    ,"Eliminar Cita",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCancelarMouseClicked

